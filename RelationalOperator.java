package JavaProgarm;

public class RelationalOperator 
{
public static void main(String args[])
{
int a=50,b=20;
  System.out.println("Equality Operator: "+(a==b));
  System.out.println("Not equal to is "+(a!=b));
  System.out.println("Greater than is "+(a>b));
  System.out.println("Less than is "+(a<b));
  System.out.println("Greater than equal to is "+(a>=b));
  System.out.println("Less than equal to is "+(a<=b));
}
}
